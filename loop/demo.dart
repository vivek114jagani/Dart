void main() {
  int i = 1;
  // do while loop
  print("do while loop");
  do 
  {
    print(i);
    i++;
  } while (i < 11);

  // while loop
  i = 5;
  print("while loop");
  while (i <= 10) 
  {
    print(i);
    i++;
  }

  // for loop
  print("for loop");
  for (i = 0; i < 10; i++) 
  {
    print(i);
  }
}
